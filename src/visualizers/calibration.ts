function clamp(value: number, min: number, max: number) {
  return Math.min(max, Math.max(min, value))
}

export function normalizeByQuantiles(
  value: number,
  stats: QuantileStats,
  low: keyof QuantileStats = 'p25',
  high: keyof QuantileStats = 'p95',
) {
  const lower = stats[low]
  const upper = stats[high]
  if (upper <= lower) return 0
  return clamp((value - lower) / (upper - lower), 0, 1)
}

export function adaptiveAlpha(windowMs: number, frameMs: number) {
  if (windowMs <= 0 || frameMs <= 0) return 0
  return 1 - Math.exp(-frameMs / windowMs)
}

